CREATE OR REPLACE VIEW VW_ACCOUNT_LEDGER AS
SELECT
    h.ID AS EXP_DOC_ID,
    l.ID AS EXP_LINE_ID,
    r.ID AS EXP_RATE_ID,
    h.TENANT_ID,
    h.CO_ID,
    l.DEPT_ID,
    l.EMP_ID,
    l.EXP_YEAR,
    l.EXP_MONTH,
    l.EXP_DT,
    l.EXP_PLACE_NM,
    'NET' AS LINE_TP_CD,
    CAST(l.EXP_ACC_CD AS CHAR) AS ACC_CD,
    CAST(l.EXP_PUR_CD AS CHAR) AS CONTRA_ACC_CD,
    acc.ACC_NM AS ACC_NM,
    acc.REF_DESC_1 AS ACC_REF_DESC_1,
    ABS(IFNULL(r.EXP_EXCL_AMT, 0)) AS AMT,
    IFNULL(r.EXP_RATE, 0) AS EXP_RATE,
    CASE
        WHEN IFNULL(r.EXP_EXCL_AMT, 0) < 0 THEN CASE WHEN acc.DRCR_CD = 'D' THEN 'C' ELSE 'D' END
        ELSE IFNULL(acc.DRCR_CD, 'C')
    END AS DRCR_CD,
    h.APRV_STS_CD AS STS_CD,
    IFNULL(a.SAVE_PATH, '') AS SAVE_PATH,
    IFNULL(a.SAVE_FILE_NM, '') AS SAVE_FILE_NM,
    IFNULL(a.FILE_EXT_NM, '') AS FILE_EXT_NM,
    l.FILE_NM AS FILE_NM,
    NULL AS CORP_TXN_ID
FROM TB_EXP_LINE l
JOIN TB_EXP_RATE r ON r.EXP_LINE_ID = l.ID
JOIN TB_EXP_DOC h ON h.ID = l.EXP_DOC_ID
LEFT JOIN TB_FIN_ACC acc ON acc.ID = l.ACC_ID
LEFT JOIN TB_EXP_ATTACH a ON a.EXP_LINE_ID = l.ID
WHERE l.STS_CD = 'ACTIVE'

UNION ALL

SELECT
    h.ID AS EXP_DOC_ID,
    l.ID AS EXP_LINE_ID,
    r.ID AS EXP_RATE_ID,
    h.TENANT_ID,
    h.CO_ID,
    l.DEPT_ID,
    l.EMP_ID,
    l.EXP_YEAR,
    l.EXP_MONTH,
    l.EXP_DT,
    l.EXP_PLACE_NM,
    'GROSS' AS LINE_TP_CD,
    CAST(l.EXP_PUR_CD AS CHAR) AS ACC_CD,
    CAST(l.EXP_ACC_CD AS CHAR) AS CONTRA_ACC_CD,
    pur.ACC_NM AS ACC_NM,
    pur.REF_DESC_1 AS ACC_REF_DESC_1,
    ABS(IFNULL(r.EXP_INCL_AMT, 0)) AS AMT,
    IFNULL(r.EXP_RATE, 0) AS EXP_RATE,
    CASE
        WHEN IFNULL(r.EXP_INCL_AMT, 0) < 0 THEN CASE WHEN pur.DRCR_CD = 'D' THEN 'C' ELSE 'D' END
        ELSE IFNULL(pur.DRCR_CD, 'C')
    END AS DRCR_CD,
    h.APRV_STS_CD AS STS_CD,
    IFNULL(a.SAVE_PATH, '') AS SAVE_PATH,
    IFNULL(a.SAVE_FILE_NM, '') AS SAVE_FILE_NM,
    IFNULL(a.FILE_EXT_NM, '') AS FILE_EXT_NM,
    l.FILE_NM AS FILE_NM,
    NULL AS CORP_TXN_ID
FROM TB_EXP_LINE l
JOIN TB_EXP_RATE r ON r.EXP_LINE_ID = l.ID
JOIN TB_EXP_DOC h ON h.ID = l.EXP_DOC_ID
LEFT JOIN TB_FIN_ACC pur ON pur.ID = l.PUR_ACC_ID
LEFT JOIN TB_EXP_ATTACH a ON a.EXP_LINE_ID = l.ID
WHERE l.STS_CD = 'ACTIVE'

UNION ALL

SELECT
    h.ID AS EXP_DOC_ID,
    l.ID AS EXP_LINE_ID,
    r.ID AS EXP_RATE_ID,
    h.TENANT_ID,
    h.CO_ID,
    l.DEPT_ID,
    l.EMP_ID,
    l.EXP_YEAR,
    l.EXP_MONTH,
    l.EXP_DT,
    l.EXP_PLACE_NM,
    'TAX' AS LINE_TP_CD,
    CASE
        WHEN r.EXP_RATE = 19.00 THEN '1576'
        WHEN r.EXP_RATE = 7.00 THEN '1571'
        ELSE NULL
    END AS ACC_CD,
    CAST(l.EXP_PUR_CD AS CHAR) AS CONTRA_ACC_CD,
    tax.ACC_NM AS ACC_NM,
    tax.REF_DESC_1 AS ACC_REF_DESC_1,
    ABS(IFNULL(r.EXP_TAX_AMT, 0)) AS AMT,
    IFNULL(r.EXP_RATE, 0) AS EXP_RATE,
    CASE
        WHEN IFNULL(r.EXP_TAX_AMT, 0) < 0 THEN CASE WHEN tax.DRCR_CD = 'D' THEN 'C' ELSE 'D' END
        ELSE IFNULL(tax.DRCR_CD, 'D')
    END AS DRCR_CD,
    h.APRV_STS_CD AS STS_CD,
    IFNULL(a.SAVE_PATH, '') AS SAVE_PATH,
    IFNULL(a.SAVE_FILE_NM, '') AS SAVE_FILE_NM,
    IFNULL(a.FILE_EXT_NM, '') AS FILE_EXT_NM,
    l.FILE_NM AS FILE_NM,
    NULL AS CORP_TXN_ID
FROM TB_EXP_LINE l
JOIN TB_EXP_RATE r ON r.EXP_LINE_ID = l.ID
JOIN TB_EXP_DOC h ON h.ID = l.EXP_DOC_ID
LEFT JOIN TB_FIN_ACC tax
    ON tax.TENANT_ID = l.TENANT_ID
   AND tax.CO_ID = l.CO_ID
   AND tax.ACC_CD = CASE WHEN r.EXP_RATE = 19.00 THEN '1576' WHEN r.EXP_RATE = 7.00 THEN '1571' ELSE NULL END
LEFT JOIN TB_EXP_ATTACH a ON a.EXP_LINE_ID = l.ID
WHERE l.STS_CD = 'ACTIVE'
  AND r.EXP_RATE IN (7.00, 19.00)

UNION ALL

SELECT
    m.EXP_DOC_ID,
    m.EXP_LINE_ID,
    m.EXP_RATE_ID,
    t.TENANT_ID,
    t.CO_ID,
    NULL AS DEPT_ID,
    NULL AS EMP_ID,
    t.TXN_YEAR AS EXP_YEAR,
    t.TXN_MONTH AS EXP_MONTH,
    DATE(t.TXN_DTM) AS EXP_DT,
    NULL AS EXP_PLACE_NM,
    'PAYMENT' AS LINE_TP_CD,
    CAST(ca.EXP_ACC_CD AS CHAR) AS ACC_CD,
    CAST(m.EXP_PUR_CD AS CHAR) AS CONTRA_ACC_CD,
    t.TXN_DESC AS ACC_NM,
    NULL AS ACC_REF_DESC_1,
    ABS(IFNULL(m.PAID_AMT, 0)) AS AMT,
    0.00 AS EXP_RATE,
    CASE
        WHEN IFNULL(t.DEPOSIT_AMT, 0) > 0 THEN CASE WHEN IFNULL(m.PAID_AMT, 0) < 0 THEN 'C' ELSE 'D' END
        WHEN IFNULL(t.WITHDRAW_AMT, 0) > 0 THEN CASE WHEN IFNULL(m.PAID_AMT, 0) < 0 THEN 'D' ELSE 'C' END
        ELSE CASE WHEN IFNULL(m.PAID_AMT, 0) < 0 THEN 'C' ELSE 'D' END
    END AS DRCR_CD,
    t.TXN_STS_CD AS STS_CD,
    '' AS SAVE_PATH,
    '' AS SAVE_FILE_NM,
    '' AS FILE_EXT_NM,
    '' AS FILE_NM,
    t.ID AS CORP_TXN_ID
FROM TB_BANK_EXP_MAP m
JOIN TB_CORP_TXN t ON t.ID = m.CORP_TXN_ID
JOIN TB_CORP_ACC ca ON ca.ID = t.CORP_ACC_ID
WHERE m.STS_CD = 'ACTIVE'
  AND t.STS_CD = 'ACTIVE'

UNION ALL

SELECT
    m.EXP_DOC_ID,
    m.EXP_LINE_ID,
    m.EXP_RATE_ID,
    t.TENANT_ID,
