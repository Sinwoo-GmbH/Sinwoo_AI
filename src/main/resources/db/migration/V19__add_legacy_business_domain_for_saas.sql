-- Adds the AS-IS Sinwoo business domain to the TO-BE SaaS schema.
--
-- Source basis:
-- - AS-IS dump: sinwoo_prod tables from sinwoosql1.sql.
-- - AS-IS app: Sinwoo-New MyBatis mappers under src/main/resources/mybatis/mapper.
-- - TO-BE dump: current sinwoo tables from sinwoosql2.sql.
--
-- Design rules:
-- - Reuse existing SaaS core tables: TB_TENANT, TB_CO, TB_DEPT, TB_EMP, TB_USR, TB_ATTND, TB_HOLI.
-- - Add TENANT_ID and CO_ID to every company-scoped business table.
-- - Keep LEGACY_* identifiers to support deterministic data migration from AS-IS string/int keys.
-- - Convert AS-IS soft deletes into STS_CD / USE_YN, while preserving legacy flags when needed.
-- - Replace AS-IS missing primary keys on attachment tables with surrogate IDs.

CREATE TABLE TB_LEGACY_MAP (
    ID BIGINT NOT NULL AUTO_INCREMENT COMMENT 'Legacy mapping primary key',
    TENANT_ID BIGINT NOT NULL COMMENT 'Tenant primary key reference',
    CO_ID BIGINT NULL COMMENT 'Company primary key reference',
    SRC_TBL_NM VARCHAR(100) NOT NULL COMMENT 'Legacy source table name',
    SRC_PK_VAL VARCHAR(255) NOT NULL COMMENT 'Legacy source primary key value',
    TGT_TBL_NM VARCHAR(100) NOT NULL COMMENT 'Target table name',
    TGT_ID BIGINT NULL COMMENT 'Target primary key value',
    LEGACY_CO_ID VARCHAR(20) NULL COMMENT 'Legacy company id',
    LEGACY_DEPT_ID VARCHAR(20) NULL COMMENT 'Legacy department id',
    LEGACY_EMP_ID VARCHAR(50) NULL COMMENT 'Legacy employee id',
    MAP_STS_CD VARCHAR(20) NOT NULL DEFAULT 'MAPPED' COMMENT 'Mapping status code',
    NOTE VARCHAR(1000) NULL COMMENT 'Mapping note',
    CRT_BY VARCHAR(100) NOT NULL DEFAULT 'SYSTEM' COMMENT 'Created by',
    CRT_DTM TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Created datetime',
    UPD_BY VARCHAR(100) NOT NULL DEFAULT 'SYSTEM' COMMENT 'Updated by',
    UPD_DTM TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Updated datetime',
