    CONSTRAINT UK_TB_LEGACY_MAP_01 UNIQUE (TENANT_ID, SRC_TBL_NM, SRC_PK_VAL, TGT_TBL_NM),
    CONSTRAINT FK_TB_LEGACY_MAP_01 FOREIGN KEY (TENANT_ID) REFERENCES TB_TENANT (ID),
    CONSTRAINT FK_TB_LEGACY_MAP_02 FOREIGN KEY (CO_ID) REFERENCES TB_CO (ID)
