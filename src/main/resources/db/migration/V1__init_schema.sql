CREATE TABLE TB_TENANT (
    ID BIGINT NOT NULL AUTO_INCREMENT COMMENT 'Tenant primary key',
    TENANT_CD VARCHAR(100) NOT NULL COMMENT 'Tenant code',
    TENANT_NM VARCHAR(255) NOT NULL COMMENT 'Tenant name',
    STS_CD VARCHAR(20) NOT NULL COMMENT 'Status code',
    CRT_BY VARCHAR(100) NOT NULL DEFAULT 'SYSTEM' COMMENT 'Created by',
    CRT_DTM TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Created datetime',
    UPD_BY VARCHAR(100) NOT NULL DEFAULT 'SYSTEM' COMMENT 'Updated by',
    UPD_DTM TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Updated datetime',
    CONSTRAINT PK_TB_TENANT PRIMARY KEY (ID),
    CONSTRAINT UK_TB_TENANT_TENANT_CD UNIQUE (TENANT_CD)
) COMMENT='Tenant master';

CREATE TABLE TB_CO (
    ID BIGINT NOT NULL AUTO_INCREMENT COMMENT 'Company primary key',
    TENANT_ID BIGINT NOT NULL COMMENT 'Tenant primary key reference',
    CO_NM VARCHAR(255) NOT NULL COMMENT 'Company name',
    REG_NO VARCHAR(100) NULL COMMENT 'Registration number',
    STS_CD VARCHAR(20) NOT NULL COMMENT 'Status code',
    CRT_BY VARCHAR(100) NOT NULL DEFAULT 'SYSTEM' COMMENT 'Created by',
    CRT_DTM TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Created datetime',
    UPD_BY VARCHAR(100) NOT NULL DEFAULT 'SYSTEM' COMMENT 'Updated by',
    UPD_DTM TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Updated datetime',
    CONSTRAINT PK_TB_CO PRIMARY KEY (ID),
    CONSTRAINT FK_TB_CO_01 FOREIGN KEY (TENANT_ID) REFERENCES TB_TENANT (ID)
) COMMENT='Company master';

CREATE TABLE TB_USR (
    ID BIGINT NOT NULL AUTO_INCREMENT COMMENT 'User primary key',
    TENANT_ID BIGINT NOT NULL COMMENT 'Tenant primary key reference',
    CO_ID BIGINT NULL COMMENT 'Company primary key reference',
    EML VARCHAR(255) NOT NULL COMMENT 'User email',
    PWD_HASH VARCHAR(255) NOT NULL COMMENT 'Password hash value',
    DSP_NM VARCHAR(255) NOT NULL COMMENT 'Display name',
    LOCL_CD VARCHAR(10) NOT NULL COMMENT 'Locale code',
    STS_CD VARCHAR(20) NOT NULL COMMENT 'Status code',
    CRT_BY VARCHAR(100) NOT NULL DEFAULT 'SYSTEM' COMMENT 'Created by',
    CRT_DTM TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Created datetime',
