CREATE TABLE TB_USR_ROLE (
    ID BIGINT NOT NULL AUTO_INCREMENT COMMENT 'User role mapping primary key',
    USR_ID BIGINT NOT NULL COMMENT 'User primary key reference',
    ROLE_ID BIGINT NOT NULL COMMENT 'Role primary key reference',
