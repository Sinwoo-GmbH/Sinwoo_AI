    'I', COALESCE(NEW.UPD_BY, NEW.CRT_BY, 'SYSTEM'), NEW.ID, NEW.TENANT_ID, NEW.CO_ID, NEW.USR_ID, NEW.EMP_ID, NEW.ATTND_DT, NEW.CHKIN_DTM, NEW.CHKOUT_DTM, NEW.ATTND_STS_CD, NEW.CRT_BY, NEW.CRT_DTM, NEW.UPD_BY, NEW.UPD_DTM
