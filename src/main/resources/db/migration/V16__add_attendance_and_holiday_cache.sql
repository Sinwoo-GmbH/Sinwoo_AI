    HOLI_DT DATE NOT NULL COMMENT 'Holiday date',
    HOLI_NM_LOC VARCHAR(255) NOT NULL COMMENT 'Holiday local name',
    HOLI_NM_EN VARCHAR(255) NOT NULL COMMENT 'Holiday English name',
    GLOB_YN CHAR(1) NOT NULL DEFAULT 'N' COMMENT 'Global holiday yes or no',
    SRC_NM VARCHAR(50) NOT NULL COMMENT 'Source name',
