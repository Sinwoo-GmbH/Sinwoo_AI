) VALUES (
    'U', COALESCE(NEW.UPD_BY, NEW.CRT_BY, 'SYSTEM'), NEW.ID, NEW.USR_ID, NEW.TENANT_ID, NEW.OAUTH_PROV_CD, NEW.OAUTH_SUB, NEW.EML, NEW.EML_VRFY_YN, NEW.LAST_LGN_DTM, NEW.CRT_BY, NEW.CRT_DTM, NEW.UPD_BY, NEW.UPD_DTM
);

