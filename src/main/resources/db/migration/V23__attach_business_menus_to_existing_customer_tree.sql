UPDATE TB_MNU
SET UP_MNU_ID = @BIZ_FIN_MGT_ID,
    PATH_URI = CONCAT('/customer/finance/management/', CASE MNU_CD
        WHEN 'MNU_BIZ_MST_ACC' THEN 'account-codes'
        WHEN 'MNU_BIZ_MST_CORP_ACC' THEN 'corporate-accounts'
        WHEN 'MNU_BIZ_MST_DAILY_ALLOWANCE' THEN 'daily-allowance'
        WHEN 'MNU_BIZ_MST_ACC_MAPPING' THEN 'account-mapping'
        WHEN 'MNU_BIZ_MST_FI_STMT' THEN 'financial-statements'
    END),
    DSP_ORD = CASE MNU_CD
        WHEN 'MNU_BIZ_MST_ACC' THEN 70
        WHEN 'MNU_BIZ_MST_CORP_ACC' THEN 80
        WHEN 'MNU_BIZ_MST_DAILY_ALLOWANCE' THEN 90
        WHEN 'MNU_BIZ_MST_ACC_MAPPING' THEN 100
        WHEN 'MNU_BIZ_MST_FI_STMT' THEN 110
        ELSE DSP_ORD
    END,
    BILL_GATE_CD = 'PAID_CUSTOMER_ADMIN',
    USE_YN = 'Y',
    UPD_BY = 'SYSTEM',
    UPD_DTM = CURRENT_TIMESTAMP
WHERE MNU_CD IN (
    'MNU_BIZ_MST_ACC',
    'MNU_BIZ_MST_CORP_ACC',
    'MNU_BIZ_MST_DAILY_ALLOWANCE',
    'MNU_BIZ_MST_ACC_MAPPING',
    'MNU_BIZ_MST_FI_STMT'
);
