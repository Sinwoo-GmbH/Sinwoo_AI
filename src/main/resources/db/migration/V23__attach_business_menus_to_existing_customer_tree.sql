-- Rehomes AS-IS business menus into the existing TO-BE customer menu tree.
-- V21 exposed the 업무 surface as standalone business roots; the SaaS shell already
-- has Team, HR, and Finance roots, so business leaves must live under those roots.

SET @CUSTOMER_TEAM_ID = (SELECT ID FROM TB_MNU WHERE MNU_CD = 'MNU_CUSTOMER_TEAM' LIMIT 1);
SET @CUSTOMER_HR_ID = (SELECT ID FROM TB_MNU WHERE MNU_CD = 'MNU_CUSTOMER_HR' LIMIT 1);
SET @CUSTOMER_FIN_ID = (SELECT ID FROM TB_MNU WHERE MNU_CD = 'MNU_CUSTOMER_FIN' LIMIT 1);
SET @BIZ_FIN_MGT_ID = (SELECT ID FROM TB_MNU WHERE MNU_CD = 'MNU_BIZ_FIN_MGT' LIMIT 1);

UPDATE TB_MNU
SET USE_YN = 'N',
    UPD_BY = 'SYSTEM',
    UPD_DTM = CURRENT_TIMESTAMP
WHERE MNU_CD IN (
    'MNU_BIZ_REQUESTS',
    'MNU_BIZ_MY_CLAIMS',
    'MNU_BIZ_REPORTS',
    'MNU_BIZ_MASTER',
    'MNU_BIZ_REQ_WORK_TIME',
    'MNU_BIZ_REQ_LEAVE',
    'MNU_BIZ_MST_EMP',
    'MNU_BIZ_MST_DEPT',
    'MNU_BIZ_FIN_MGT_EXPENSE'
);

UPDATE TB_ROLE_MNU_AUTH a
JOIN TB_MNU m ON m.ID = a.MNU_ID
SET a.VIEW_YN = 'N',
    a.CRT_YN = 'N',
    a.UPD_YN = 'N',
    a.DEL_YN = 'N',
    a.APRV_YN = 'N',
    a.EXPRT_YN = 'N',
    a.UPD_BY = 'SYSTEM',
    a.UPD_DTM = CURRENT_TIMESTAMP
WHERE m.MNU_CD IN (
    'MNU_BIZ_REQUESTS',
    'MNU_BIZ_MY_CLAIMS',
    'MNU_BIZ_REPORTS',
    'MNU_BIZ_MASTER',
    'MNU_BIZ_REQ_WORK_TIME',
    'MNU_BIZ_REQ_LEAVE',
    'MNU_BIZ_MST_EMP',
    'MNU_BIZ_MST_DEPT',
    'MNU_BIZ_FIN_MGT_EXPENSE'
);

UPDATE TB_MNU
SET UP_MNU_ID = @CUSTOMER_TEAM_ID,
