SELECT r.ID,
       m.ID,
       'Y',
       CASE WHEN r.ROLE_CD = 'ROLE_PLATFORM_SUPER_ADMIN' THEN 'Y'
            WHEN m.MNU_CD IN ('MNU_ADMIN_TENANT','MNU_ADMIN_AUTH','MNU_ADMIN_BILL','MNU_ADMIN_MENU','MNU_ADMIN_AUDIT') THEN 'Y'
            ELSE 'N' END,
       CASE WHEN r.ROLE_CD = 'ROLE_PLATFORM_SUPER_ADMIN' THEN 'Y'
            WHEN m.MNU_CD IN ('MNU_ADMIN_TENANT','MNU_ADMIN_AUTH','MNU_ADMIN_BILL','MNU_ADMIN_MENU','MNU_ADMIN_AUDIT') THEN 'Y'
            ELSE 'N' END,
       CASE WHEN r.ROLE_CD = 'ROLE_PLATFORM_SUPER_ADMIN' THEN 'Y' ELSE 'N' END,
       CASE WHEN r.ROLE_CD = 'ROLE_PLATFORM_SUPER_ADMIN' THEN 'Y'
            WHEN m.MNU_CD IN ('MNU_ADMIN_BILL','MNU_ADMIN_AUDIT') THEN 'Y'
            ELSE 'N' END,
       CASE WHEN r.ROLE_CD = 'ROLE_PLATFORM_SUPER_ADMIN' THEN 'Y'
            WHEN m.MNU_CD IN ('MNU_ADMIN_TENANT','MNU_ADMIN_BILL','MNU_ADMIN_AUDIT') THEN 'Y'
            ELSE 'N' END,
       'SYSTEM',
       'SYSTEM'
FROM TB_ROLE r
JOIN TB_MNU m ON m.MNU_SCOPE_CD = 'ADMIN'
WHERE r.ROLE_CD IN ('ROLE_PLATFORM_SUPER_ADMIN', 'ROLE_PLATFORM_ADMIN')
  AND NOT EXISTS (
      SELECT 1
      FROM TB_ROLE_MNU_AUTH a
      WHERE a.ROLE_ID = r.ID
        AND a.MNU_ID = m.ID
  );
